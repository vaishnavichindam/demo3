public class AddTwoNumbers {
    public static void main(String[] args) {
        // Hardcoded numbers
        int num1 = 5;
        int num2 = 7;

        // Calculate the sum
        int sum = num1 + num2;

        // Display the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
